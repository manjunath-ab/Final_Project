    private void populatebycomm(){
        ObjectContainer db = Db4o.openFile("orders.db4o");
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Query query = db.query();
        query.constrain(Order.class);
        Constraint constr = query.descend("location").constrain(location);
        Constraint constr1=query.descend("status").constrain("Not Started");
        query.descend("dagent").constrain("Unassigned").and(constr).and(constr1);
        ObjectSet result=query.execute();
        while(result.hasNext()){
        Order o = (Order) result.next(); 
        Object[] row = new Object[1000];//2 members for now
            //row[0]=e.getName();
            row[1]=o;//1st column stores object names so..they get deleted
            row[0]=o.getOrderID();
            //need to put from location and to location.
            row[2]=o.getItemID();
            row[3]=o.getQuantity();
            row[4]=o.getStatus();
            //row[3]=o.getLocation();
            model.addRow(row);
        }
        
        db.close();
    }
